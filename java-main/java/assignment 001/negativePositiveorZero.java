public class negativePositiveorZero {
    public static void main(String args[]) {
        int n = -12;
        if(n < 0){
            System.out.println("it is negative");
        } else if (n == 0){
            System.out.println("it is zero");
        } else {
            System.out.println("it is positive");
        }
    }
}
